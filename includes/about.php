<h1>FAQ (Frequently Asked Question)</h1>
<h2>Apa itu ESOPE?</h2>

<p><strong>ESOPE</strong> (akronim untuk <em>Esope Sistem Observasi Pasien Elektronik</em>), adalah aplikasi kalkulator NEWS sederhana. Prototipe ini dibuat untuk demonstrasi konsep ESOPE sebagai aplikasi yang membantu mengetahui intervensi lebih lanjut, dan meminimalkan kendala dalam penghitungan manual di ruangan. Nama ESOPE dipilih karena senada dengan SOP (Standard Operational Procedure) yang memang sudah familiar dan kami anggap penggunaannya unik. Kode sumber ESOPE terbuka dan bisa diakses di <a href="https://github.com/cicag/esope">github.</a></p>

<h2>Untuk siapa ESOPE?</h2>

<p><ol><li>ESOPE NEWS dapat digunakan oleh semua tenaga kesehatan</li><li>ESOPE NEWS digunakan pada pasien dewasa (>16 tahun) yang kecuali ibu hamil dan pasien cedera tulang belakang. </li></ol></p>

<h2>Dimana ESOPE bisa digunakan?</h2>
<p>Aplikasi dapat digunakan di IGD, ICU dan ruang rawat inap.</p>

<h2>Kapan menggunakan ESOPE?</h2>
<p>Aplikasi digunakan setiap kali pemantauan tanda-tanda vital rutin dan/sesuai klasifikasi sebelumnya</p>

<h2>Kenapa menggunakan ESOPE?</h2>
<p>Seperti sudah disebutkan sebelumnya, ESOPE dapat digunakan untuk membantu mengetahui intervensi lebih lanjut, dan meminimalkan kendala dalam penghitungan manual di ruangan.</p>

<h2>Bagaimana menggunakan ESOPE?</h2>
<p>
Cara Penggunaan:
<ol>
<li>Buka aplikasi melalui browser (Chrome, Edge,dsb),</li>
<li>Masukkan parameter fisiologis sesuai kondisi pasien,</li>
<li>Tekan tombol “hitung” untuk mulai menghitung,</li>
<li>Tekan tombol “reset” untuk memulai perhitungan baru.</li>
</ol></p>

